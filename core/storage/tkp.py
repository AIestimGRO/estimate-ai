class TkpSourceRecord:
    """A stored `tkp_sources` row, as shown in the admin UI."""

    id: int
    run_id: str
    file_name: str
    file_path: str
    modified_date: str
    parse_status: str
    parse_message: str
    task_no: str
    customer: str
    procedure_name: str
    winner_name: str
    winner_total_no_vat: float | None
    item_count: int
    imported_at: str


