"""Persistence for the TKP (tender winner) catalog.

Delta-import model: `import_tkp_catalog_workbook` can be pointed at the same
aggregate `Каталог_ТКПшек*.xlsm` workbook every time it is regenerated by
the KL20 CatalogBuilder macro (which re-scans the whole watched folder, not
just new files). A source file is only re-imported when it is new or its
`ModifiedDate` changed; unchanged files are skipped, keeping repeated
imports of the same workbook cheap and idempotent -- the RNMC import
(`core/storage/catalog.py`) uses the same dedup shape, filename-keyed there
since RNMC has no reliable modified-date signal.
"""

from __future__ import annotations

import sqlite3
from dataclasses import dataclass
from pathlib import Path

from core.tkp_ingest import (
    TkpCatalogParseResult,
    TkpItem,
    TkpSourceFile,
    parse_tkp_catalog_workbook,
)

TKP_DETAILS_VERSION = 1

TKP_SORT_COLUMNS = {
    "item_name": "tkp_items.item_name",
    "unit": "tkp_items.unit",
    "qty": "tkp_items.qty",
    "rnmc_unit_price_no_vat": "tkp_items.rnmc_unit_price_no_vat",
    "winner_unit_price_no_vat": "tkp_items.winner_unit_price_no_vat",
    "winner_line_total_no_vat": "tkp_items.winner_line_total_no_vat",
    "winner_name": "tkp_items.winner_name",
    "task_no": "tkp_items.task_no",
    "request_date": "tkp_items.request_date",
    "customer": "tkp_items.customer",
    "procedure_name": "tkp_items.procedure_name",
    "source_file_name": "tkp_sources.file_name",
}


@dataclass(frozen=True)
class TkpImportResult:
    """Summary of one import run against the TKP catalog tables."""

    files_seen: int
    files_imported: int
    files_updated: int
    files_skipped: int
    items_imported: int


